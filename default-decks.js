function addDefaultDecksToLocalStorageOnly(userId) {
    // Mevcut desteleri localStorage'dan al
    let decks = JSON.parse(localStorage.getItem('memo-decks')) || [];
    
    // Varsayılan desteleri kullanıcı ID'si ile kopyala
    defaultDecks.forEach(deck => {
        // Her kart için benzersiz ID oluştur
        const cardsWithIds = deck.cards.map(card => ({
            id: 'card-' + Date.now() + '-' + Math.floor(Math.random() * 10000),
            front: card.front,
            back: card.back,
            createdAt: new Date().toISOString()
        }));
        
        // Yeni deste oluştur
        const newDeck = {
            id: 'deck-' + Date.now() + '-' + Math.floor(Math.random() * 10000),
            userId: userId,
            name: deck.name,
            description: deck.description,
            createdAt: new Date().toISOString(),
            cards: cardsWithIds
        };
        
        // Destelere ekle
        decks.push(newDeck);
    });
    
    // Güncellenmiş desteleri localStorage'a kaydet
    localStorage.setItem('memo-decks', JSON.stringify(decks));
    
    return true;
}

